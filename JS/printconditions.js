function main1(grade) {
    if (grade < 60) {
        console.log("D");

    } else if (grade < 70) {
        console.log("C");

    } else if (grade < 80) {
        console.log("B");

    } else if (grade < 90) {
        console.log("A");
    }
    else {
        console.log("AA")
    }

}

main1(90)